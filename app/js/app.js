// PWA Service Worker Registration
if ('serviceWorker' in navigator) {
  window.addEventListener('load', () => {
    navigator.serviceWorker.register('/app/sw.js').then((registration) => {
      console.log('ServiceWorker registration successful with scope: ', registration.scope);
    }, (err) => {
      console.log('ServiceWorker registration failed: ', err);
    });
  });
}

// Page Transition Logic
document.addEventListener("DOMContentLoaded", () => {
  // Handle splash screen visibility on index
  const path = window.location.pathname;
  if (path.endsWith('index.html') || path.endsWith('/app') || path.endsWith('/app/') || path === '/') {
    initSplashAndLogin();
  }

  // Fade in transition
  const transition = document.createElement('div');
  transition.className = 'page-transition';
  document.body.appendChild(transition);
  
  setTimeout(() => {
    transition.classList.add('loaded');
  }, 50);

  // Handle Bottom Nav Active State
  const navItems = document.querySelectorAll('.nav-item');
  const currentPath = window.location.pathname;
  navItems.forEach(item => {
    if (item.getAttribute('href') && currentPath.includes(item.getAttribute('href'))) {
      item.classList.add('active');
    }
  });
});

function navigateTo(url) {
  const transition = document.querySelector('.page-transition');
  if (transition) {
    transition.classList.remove('loaded');
    setTimeout(() => {
      window.location.href = url;
    }, 300);
  } else {
    window.location.href = url;
  }
}

// Fix for Back Button (BFCache)
window.addEventListener('pageshow', (event) => {
  const transition = document.querySelector('.page-transition');
  if (transition) {
    transition.classList.add('loaded'); // Always force it back to transparent
  }
});

/* ==========================================================================
   FAVORITES LOGIC
   ========================================================================== */
function getFavorites() {
  const favs = localStorage.getItem('favoritos_jc');
  return favs ? JSON.parse(favs) : [];
}

function toggleFavorite(id) {
  let favs = getFavorites();
  if (favs.includes(id)) {
    favs = favs.filter(favId => favId !== id);
  } else {
    favs.push(id);
  }
  localStorage.setItem('favoritos_jc', JSON.stringify(favs));
  return favs.includes(id); // return new state
}

function isFavorite(id) {
  return getFavorites().includes(id);
}

/* ==========================================================================
   LOGIN & SPLASH LOGIC (index.html)
   ========================================================================== */
function initSplashAndLogin() {
  const splash = document.getElementById('splash-screen');
  const loginForm = document.getElementById('login-screen');
  const savedEmail = localStorage.getItem('userEmail');

  // Show splash for 1.5s
  setTimeout(() => {
    if (splash) splash.style.opacity = '0';
    
    setTimeout(() => {
      if (splash) splash.style.display = 'none';
      
      if (savedEmail) {
        // Already logged in
        navigateTo('home.html');
      } else {
        // Show login
        if (loginForm) {
          loginForm.style.display = 'flex';
          setTimeout(() => loginForm.classList.add('fade-in'), 50);
        }
      }
    }, 300); // Wait for fade out
  }, 1500);

  const form = document.getElementById('access-form');
  const emailInput = document.getElementById('email-input');
  const errorMsg = document.getElementById('error-msg');

  if (form) {
    form.addEventListener('submit', (e) => {
      e.preventDefault();
      const email = emailInput.value.trim();
      
      if (email === '' || !email.includes('@') || !email.includes('.')) {
        errorMsg.style.display = 'block';
      } else {
        errorMsg.style.display = 'none';
        localStorage.setItem('userEmail', email);
        navigateTo('home.html');
      }
    });
  }
}
